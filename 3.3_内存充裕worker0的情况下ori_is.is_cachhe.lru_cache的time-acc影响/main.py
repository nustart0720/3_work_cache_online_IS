def recompu_loss(update_loss_train_loader, model, criterion):
    for i, ((images, target), img_idx) in enumerate(update_loss_train_loader):
        update_loss_dict = {}
        if args.gpu is not None:
            images = images.cuda(args.gpu, non_blocking=True)
        if torch.cuda.is_available():
            target = target.cuda(args.gpu, non_blocking=True)

        # compute output
        output = model(images)
        loss = criterion(output, target) # 修改了criterion返回向量，包含每张图的loss
        
        # 每张图片前传，记录loss
        for j in range(len(images)):
            update_loss_dict[img_idx[j]] = loss[j]
        
        update_loss_train_loader.sampler.update_loss(update_loss_dict)

